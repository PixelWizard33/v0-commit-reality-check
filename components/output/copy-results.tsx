"use client"

import { useState } from "react"
import { Copy, Check } from "lucide-react"
import type { AnalysisResult } from "@/lib/mock-data"

interface CopyResultsProps {
  result: AnalysisResult | null
  visible: boolean
}

export function CopyResults({ result, visible }: CopyResultsProps) {
  const [copied, setCopied] = useState(false)

  if (!visible || !result) return null

  const handleCopy = async () => {
    const lines: string[] = [
      "=== COMMIT REALITY CHECK ===",
      "",
      `Chaos Score: ${result.chaosScore}/100`,
      `Archetype: ${result.archetype}`,
      `Debug Pain: ${result.debugPainRating}`,
      "",
      "--- ROASTS ---",
      ...result.roasts.flatMap((r) => [`> ${r.original}`, r.roast, ""]),
      "--- SUGGESTED REWRITES ---",
      ...result.realityChecks.flatMap((c) => [`> ${c.original}`, `  ${c.suggestedRewrite}`, ""]),
      "",
      "Generated by GitKraken Commit Reality Check",
    ]
    await navigator.clipboard.writeText(lines.join("\n"))
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  return (
    <button
      onClick={handleCopy}
      className="flex items-center gap-2 border border-neon-green/40 px-3 py-2 text-xs tracking-wider text-neon-green transition-all hover:bg-neon-green/10"
    >
      {copied ? (
        <>
          <Check className="h-3 w-3" />
          COPIED TO CLIPBOARD
        </>
      ) : (
        <>
          <Copy className="h-3 w-3" />
          COPY ALL RESULTS
        </>
      )}
    </button>
  )
}
